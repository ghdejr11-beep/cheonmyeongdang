def generate_all():
    """각 템플릿의 마크다운 + 상품 설명 + 메타데이터 생성"""
    catalog = []

    for t in TEMPLATES:
        # 1. 마크다운 구조 파일
        md_path = os.path.join(OUTPUT, f"{t['id']}_structure.md")
        with open(md_path, 'w', encoding='utf-8') as f:
            f.write('\n'.join(t['structure']))
        print(f"  [MD] {md_path}")

        # 2. 상품 설명 파일
        desc_path = os.path.join(OUTPUT, f"{t['id']}_listing.md")
        with open(desc_path, 'w', encoding='utf-8') as f:
            f.write(f"# {t['name_kr']}\n\n")
            f.write(f"**{t['name_en']}**\n\n")
            f.write(f"가격: {t['price_krw']:,}원 / ${t['price_usd']}\n\n")
            f.write(f"---\n\n")
            f.write(t['description_kr'])
            f.write(f"\n\n---\n\n")
            f.write(f"카테고리: {t['category']}\n")
            f.write(f"태그: {', '.join(t['tags'])}\n")
            f.write(f"\n판매자: 쿤스튜디오\n")
        print(f"  [LIST] {desc_path}")

        catalog.append({
            "id": t['id'],
            "name_kr": t['name_kr'],
            "name_en": t['name_en'],
            "price_usd": t['price_usd'],
            "price_krw": t['price_krw'],
            "category": t['category'],
            "tags": t['tags'],
        })

    # 3. 전체 카탈로그 JSON
    cat_path = os.path.join(OUTPUT, 'catalog.json')
    with open(cat_path, 'w', encoding='utf-8') as f:
        json.dump(catalog, f, ensure_ascii=False, indent=2)
    print(f"\n  [CATALOG] {cat_path}")
    print(f"\n[OK] {len(TEMPLATES)}개 템플릿 자료 생성 완료")


if __name__ == '__main__':
    generate_all()
