def main():
    all_prompts = []

    def add_category(cat_id: str, cat_name: str, items: list):
        for i, item in enumerate(items, 1):
            all_prompts.append({
                "category": cat_id,
                "category_name": cat_name,
                "num": i,
                **item,
            })

    add_category("email", "📧 이메일 마케팅 & 세일즈", EMAIL)
    add_category("copywriting", "✍️ 카피라이팅 & 광고", COPYWRITING)

    config.PROMPTS_JSON.write_text(
        json.dumps({"prompts": all_prompts}, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(f"✓ {len(all_prompts)}개 샘플 프롬프트 저장: {config.PROMPTS_JSON}")
    print()
    print("다음 단계 (2가지 옵션):")
    print("  A. 지금 샘플 PDF 만 빌드:")
    print("     python make_prompt_pdf.py")
    print()
    print("  B. Claude API 로 나머지 980개 생성 후 전체 빌드:")
    print("     python generate_prompts.py  # 15~30분, 약 $1~2")
    print("     python make_prompt_pdf.py")


if __name__ == "__main__":
    main()
