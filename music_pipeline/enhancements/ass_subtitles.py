# ============================================================
# 시간 변환
# ============================================================
def _seconds_to_ass_time(seconds: float) -> str:
    """ASS 시간 포맷: H:MM:SS.cc (1/100초)."""
    if seconds < 0:
        seconds = 0
    total_cs = int(round(seconds * 100))
    h = total_cs // 360_000
    m = (total_cs % 360_000) // 6000
    s = (total_cs % 6000) // 100
    cs = total_cs % 100
    return f"{h}:{m:02d}:{s:02d}.{cs:02d}"


def _parse_srt_time(s: str) -> float:
    """'HH:MM:SS,mmm' → 초."""
    m = re.match(r"(\d+):(\d+):(\d+)[,.](\d+)", s.strip())
    if not m:
        return 0.0
    h, mn, sc, ms = m.groups()
    return int(h) * 3600 + int(mn) * 60 + int(sc) + int(ms) / 1000.0


def _parse_lrc_time(tag: str) -> float:
    """`[MM:SS.xx]` 또는 `[MM:SS]` → 초."""
    m = re.match(r"\[(\d+):(\d+)(?:[.:](\d+))?\]", tag)
    if not m:
        return 0.0
    mn, sc, frac = m.groups()
    t = int(mn) * 60 + int(sc)
    if frac:
        if len(frac) == 2:
            t += int(frac) / 100.0
        elif len(frac) == 3:
            t += int(frac) / 1000.0
        else:
            t += int(frac) / (10 ** len(frac))
    return t


# ============================================================
# ASS 헤더 빌드
# ============================================================
def _build_ass_header(style: dict, play_res_x: int = 1920, play_res_y: int = 1080) -> str:
    """ASS 파일 헤더 (Script Info + Styles)."""
    return f"""[Script Info]
Title: Auto-generated by music_pipeline
ScriptType: v4.00+
PlayResX: {play_res_x}
PlayResY: {play_res_y}
WrapStyle: 2
ScaledBorderAndShadow: yes
YCbCr Matrix: TV.709

[V4+ Styles]
Format: Name, Fontname, Fontsize, PrimaryColour, SecondaryColour, OutlineColour, BackColour, Bold, Italic, Underline, StrikeOut, ScaleX, ScaleY, Spacing, Angle, BorderStyle, Outline, Shadow, Alignment, MarginL, MarginR, MarginV, Encoding
Style: Default,{style['fontname']},{style['fontsize']},{style['primary']},&H000000FF,{style['outline_color']},{style['back_color']},{style['bold']},0,0,0,100,100,0,0,1,{style['outline']},{style['shadow']},{style['alignment']},60,60,{style['margin_v']},1

[Events]
Format: Layer, Start, End, Style, Name, MarginL, MarginR, MarginV, Effect, Text
"""


def _build_dialogue_line(start: float, end: float, text: str, style: dict) -> str:
    """Dialogue 줄 + 페이드 효과."""
    fade_in = style.get("fade_in_ms", 0)
    fade_out = style.get("fade_out_ms", 0)
    fade_tag = f"{{\\fad({fade_in},{fade_out})}}" if (fade_in or fade_out) else ""
    # ASS 에서 줄바꿈은 \\N
    text_escaped = text.replace("\n", r"\N").replace("{", r"\{").replace("}", r"\}")
    return (
        f"Dialogue: 0,{_seconds_to_ass_time(start)},{_seconds_to_ass_time(end)},"
        f"Default,,0,0,0,,{fade_tag}{text_escaped}"
    )


# ============================================================
# 메인 변환 함수
# ============================================================
def srt_to_ass(
    srt_path: str,
    out_ass_path: str,
    style: str = "ballad",
) -> bool:
    """기존 SRT 파일 → ASS 변환."""
    style_dict = STYLE_PRESETS.get(style) or STYLE_PRESETS["ballad"]

    try:
        content = Path(srt_path).read_text(encoding="utf-8")
    except Exception:
        return False

    entries: list[tuple[float, float, str]] = []
    blocks = re.split(r"\n\s*\n", content.strip())
    for block in blocks:
        lines = [ln.strip() for ln in block.strip().splitlines() if ln.strip()]
        if len(lines) < 2:
            continue
        # 첫 줄은 인덱스 (숫자), 두 번째 줄은 타이밍
        timing_idx = 1 if lines[0].isdigit() else 0
        if timing_idx >= len(lines):
            continue
        timing = lines[timing_idx]
        if "-->" not in timing:
            continue
        start_s, end_s = timing.split("-->", 1)
        start = _parse_srt_time(start_s)
        end = _parse_srt_time(end_s)
        text = "\n".join(lines[timing_idx + 1:]).strip()
        if text:
            entries.append((start, end, text))

    if not entries:
        return False

    header = _build_ass_header(style_dict)
    body = "\n".join(
        _build_dialogue_line(s, e, t, style_dict) for s, e, t in entries
    )

    try:
        Path(out_ass_path).write_text(header + body + "\n", encoding="utf-8")
        return True
    except Exception:
        return False


def lrc_to_ass(
    lrc_path: str,
    out_ass_path: str,
    style: str = "ballad",
    default_line_duration: float = 4.0,
) -> bool:
    """LRC 파일 → ASS 직접 변환 (SRT 경유 안 함)."""
    style_dict = STYLE_PRESETS.get(style) or STYLE_PRESETS["ballad"]

    try:
        content = Path(lrc_path).read_text(encoding="utf-8")
    except Exception:
        return False

    time_pat = re.compile(r"(\[\d+:\d+(?:[.:]\d+)?\])(.*)")
    entries: list[tuple[float, str]] = []

    for line in content.splitlines():
        line = line.strip()
        if not line:
            continue
        # [Verse], [Chorus] 섹션 태그 스킵
        if line.startswith("[") and not time_pat.match(line):
            continue
        m = time_pat.match(line)
        if not m:
            continue
        tag, text = m.groups()
        text = text.strip()
        if not text:
            continue
        start = _parse_lrc_time(tag)
        entries.append((start, text))

    if len(entries) < 1:
        return False
    entries.sort(key=lambda x: x[0])

    header = _build_ass_header(style_dict)
    lines = []
    for i, (start, text) in enumerate(entries):
        if i + 1 < len(entries):
            end = entries[i + 1][0] - 0.05
        else:
            end = start + default_line_duration
        if end <= start:
            end = start + default_line_duration
        lines.append(_build_dialogue_line(start, end, text, style_dict))

    try:
        Path(out_ass_path).write_text(header + "\n".join(lines) + "\n", encoding="utf-8")
        return True
    except Exception:
        return False


def pick_style_for(style_name: str) -> str:
    """youtube_uploader 의 style_name → ass_subtitles 프리셋 매핑."""
    if style_name in ("pop", "kpop"):
        return "pop"
    if style_name in ("lofi", "jazz", "study", "classical"):
        return "lofi"
    if style_name in ("sleep", "meditation", "rain"):
        return "emotional"
    return "ballad"


