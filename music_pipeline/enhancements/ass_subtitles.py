"""LRC/SRT → ASS (Advanced SubStation Alpha) 변환기.

ASS 는 SRT 와 달리 폰트·색상·외곽선·페이드·포지션을 자막 파일 내부에서
직접 지정할 수 있어 FFmpeg `subtitles` 필터로 렌더링할 때 훨씬 깔끔한
결과가 나온다. 2026년 유튜브 가사 비디오의 표준.

특징:
  - Montserrat Bold 권장 (한글 병기 시 Noto Sans KR Bold)
  - 중앙 하단 1/3 배치, 모바일 가독성
  - 라인 페이드 인/아웃 250ms
  - 비트 강조 옵션: 특정 라인 크기 업

lyrics_watcher.py 에서 사용:

    from music_pipeline.enhancements.ass_subtitles import (
        lrc_to_ass, srt_to_ass,
    )

    ass_path = lrc_to_ass(lrc_path, out_ass_path, style='ballad')
    # ffmpeg 에 -vf subtitles=ass_path 로 적용

현재 lyrics_watcher.py 의 `convert_lrc_to_srt` 출력을 이 모듈의
`srt_to_ass` 로 한 번 더 변환하면 된다.
"""

